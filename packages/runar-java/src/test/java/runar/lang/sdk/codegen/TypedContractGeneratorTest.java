    @Test
    void generatedP2pkhCompilesCleanly() throws Exception {
        RunarArtifact artifact = buildP2PKHArtifact();
        String src = TypedContractGenerator.generate(artifact, "runar.generated.p2pkh");
        Class<?> cls = compileInMemory("runar.generated.p2pkh.P2PKHWrapper", src);
        assertEquals("P2PKHWrapper", cls.getSimpleName());
    }

    @Test
    void generatedConstructorMatchesExpectedSignature() throws Exception {
        RunarArtifact artifact = buildP2PKHArtifact();
        String src = TypedContractGenerator.generate(artifact, "runar.generated.p2pkh");
        Class<?> cls = compileInMemory("runar.generated.p2pkh.P2PKHWrapper", src);

        Constructor<?>[] ctors = cls.getDeclaredConstructors();
        assertEquals(1, ctors.length, "expected exactly one constructor");
        Class<?>[] params = ctors[0].getParameterTypes();
        assertEquals(4, params.length, "constructor should take (artifact, pubKeyHash, provider, signer)");
        assertEquals(RunarArtifact.class, params[0]);
        assertEquals(Addr.class, params[1]);
        assertEquals(runar.lang.sdk.Provider.class, params[2]);
        assertEquals(runar.lang.sdk.Signer.class, params[3]);
    }

    @Test
    void generatedP2pkhHasVoidUnlockWithPubKeyOnly() throws Exception {
        RunarArtifact artifact = buildP2PKHArtifact();
        String src = TypedContractGenerator.generate(artifact, "runar.generated.p2pkh");
        Class<?> cls = compileInMemory("runar.generated.p2pkh.P2PKHWrapper", src);

        Method unlock = cls.getDeclaredMethod("unlock", PubKey.class);
        assertEquals(void.class, unlock.getReturnType(),
            "stateless (terminal) methods should return void");

        // Sig param must be elided from the signature (auto-computed by SDK).
        for (Method m : cls.getDeclaredMethods()) {
            if ("unlock".equals(m.getName())) {
                assertFalse(Arrays.asList(m.getParameterTypes()).contains(Sig.class),
                    "unlock() must not declare a Sig parameter");
            }
        }
    }

    // ---------------------------------------------------------------------
    // Stateful (Counter) — typed method + state accessor
    // ---------------------------------------------------------------------

    @Test
    void generatesTypedStatefulCounterWrapper() throws Exception {
        RunarArtifact artifact = buildCounterArtifact();
        String src = TypedContractGenerator.generate(artifact, "runar.generated.counter");
        Class<?> cls = compileInMemory("runar.generated.counter.CounterWrapper", src);

        // Typed increment(BigInteger amount) → String (stateful, non-terminal)
        Method increment = cls.getDeclaredMethod("increment", BigInteger.class);
        assertEquals(String.class, increment.getReturnType(),
            "stateful non-terminal methods must return the txid String");

        // Typed state accessor count() → BigInteger
        Method count = cls.getDeclaredMethod("count");
        assertEquals(BigInteger.class, count.getReturnType(),
            "state accessor for bigint field must return BigInteger");

        // Stateful reset method (terminal: no _changePKH) → void
        Method reset = cls.getDeclaredMethod("reset");
        assertEquals(void.class, reset.getReturnType(),
            "terminal stateful methods must return void");

        // Source-level sanity: cast expression for state accessor and ABI call.
        assertTrue(src.contains("(BigInteger) inner.state(\"count\")"),
            "state accessor must cast Object returned by inner.state()");
        assertTrue(src.contains("inner.call(\"increment\""),
            "generated wrapper must delegate to inner.call()");
    }

