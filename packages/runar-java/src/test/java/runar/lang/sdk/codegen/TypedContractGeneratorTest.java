    @Test
    void generatesExpectedP2pkhSource() {
        RunarArtifact artifact = buildP2PKHArtifact();
        String src = TypedContractGenerator.generate(artifact, "runar.generated.p2pkh");

        String expected = """
            // Generated by: runar codegen
            // Source: P2PKH
            // Do not edit manually.

            package runar.generated.p2pkh;

            import java.math.BigInteger;
            import java.util.ArrayList;
            import java.util.List;
            import runar.lang.sdk.Provider;
            import runar.lang.sdk.RunarArtifact;
            import runar.lang.sdk.RunarContract;
            import runar.lang.sdk.Signer;
            import runar.lang.types.Addr;
            import runar.lang.types.PubKey;

            public final class P2PKHWrapper {
                private final RunarContract inner;
                private final Provider provider;
                private final Signer signer;

                public P2PKHWrapper(RunarArtifact artifact, Addr pubKeyHash, Provider provider, Signer signer) {
                    List<Object> args = new ArrayList<>();
                    args.add(pubKeyHash.toHex());
                    this.inner = new RunarContract(artifact, args);
                    this.provider = provider;
                    this.signer = signer;
                }

                /** Returns the underlying {@link RunarContract}. */
                public RunarContract contract() {
                    return inner;
                }

                /** Deploys the contract on-chain. Returns the broadcast txid. */
                public String deploy(BigInteger satoshis) {
                    return inner.deploy(provider, signer, satoshis.longValueExact()).txid();
                }

                /** Terminal method: unlock. */
                public void unlock(PubKey pubKey) {
                    List<Object> callArgs = new ArrayList<>();
                    callArgs.add(null); // Sig auto-computed by SDK
                    callArgs.add(pubKey.toHex());
                    inner.call("unlock", callArgs, null, provider, signer);
                }
            }
            """;

        assertEquals(normalize(expected), normalize(src),
            "generated P2PKH source did not match golden\n--- actual ---\n" + src);
    }

    // ---------------------------------------------------------------------
